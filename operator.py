# --------------------------------------
# 1. Arithmetic Operators
# --------------------------------------

a = 10
b = 3

print("Arithmetic Operators:")
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a // b =", a // b)
print("a % b =", a % b)
print("a ** b =", a ** b)
print("\n")


# --------------------------------------
# 2. Comparison Operators
# --------------------------------------

print("Comparison Operators:")
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)
print("a >= b:", a >= b)
print("a <= b:", a <= b)
print("\n")


# --------------------------------------
# 3. Logical Operators
# --------------------------------------

x = True
y = False

print("Logical Operators:")
print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)
print("\n")


# --------------------------------------
# 4. Assignment Operators
# --------------------------------------

c = 5
print("Assignment Operators:")
print("Initial c =", c)

c += 3
print("c += 3 ->", c)

c -= 2
print("c -= 2 ->", c)

c *= 4
print("c *= 4 ->", c)

c /= 3
print("c /= 3 ->", c)

c **= 2
print("c **= 2 ->", c)
print("\n")


